  String _databaseServiceImplDart(ResolvedGenerationConfig resolved) {
    return '''
import 'package:injectable/injectable.dart';
import 'package:sqflite/sqflite.dart';
import '${_packageImport(resolved, 'core/services/database/app_database_service.dart')}';

@LazySingleton(as: AppDatabaseService)
class AppDatabaseServiceImpl implements AppDatabaseService {
  Database? _database;

  @override
  Future<Database> open({
    String fileName = 'app.db',
    int version = 1,
    OnDatabaseConfigureFn? onConfigure,
    OnDatabaseCreateFn? onCreate,
    OnDatabaseVersionChangeFn? onUpgrade,
    OnDatabaseVersionChangeFn? onDowngrade,
    OnDatabaseOpenFn? onOpen,
  }) async {
    if (_database != null) {
      return _database!;
    }

    final databasePath = await getDatabasesPath();
    _database = await openDatabase(
      '\$databasePath/\$fileName',
      version: version,
      onConfigure: onConfigure,
      onCreate: onCreate,
      onUpgrade: onUpgrade,
      onDowngrade: onDowngrade,
      onOpen: onOpen,
    );
    return _database!;
  }

  @override
  Future<int> create(
    String table,
    Map<String, Object?> values, {
    ConflictAlgorithm conflictAlgorithm = ConflictAlgorithm.abort,
  }) async {
    final database = await open();
    return database.insert(
      table,
      values,
      conflictAlgorithm: conflictAlgorithm,
    );
  }

  @override
  Future<List<Map<String, Object?>>> read(
    String table, {
    bool? distinct,
    List<String>? columns,
    String? where,
    List<Object?>? whereArgs,
    String? groupBy,
    String? having,
    String? orderBy,
    int? limit,
    int? offset,
  }) async {
    final database = await open();
    return database.query(
      table,
      distinct: distinct,
      columns: columns,
      where: where,
      whereArgs: whereArgs,
      groupBy: groupBy,
      having: having,
      orderBy: orderBy,
      limit: limit,
      offset: offset,
    );
  }

  @override
  Future<Map<String, Object?>?> readById(
    String table,
    Object id, {
    String idColumn = 'id',
    List<String>? columns,
  }) async {
    final rows = await read(
      table,
      columns: columns,
      where: '\$idColumn = ?',
      whereArgs: <Object?>[id],
      limit: 1,
    );

    return rows.isEmpty ? null : rows.first;
  }

  @override
  Future<int> update(
    String table,
    Map<String, Object?> values, {
    String? where,
    List<Object?>? whereArgs,
    ConflictAlgorithm conflictAlgorithm = ConflictAlgorithm.abort,
  }) async {
    final database = await open();
    return database.update(
      table,
      values,
      where: where,
      whereArgs: whereArgs,
      conflictAlgorithm: conflictAlgorithm,
    );
  }

  @override
  Future<int> delete(
    String table, {
    String? where,
    List<Object?>? whereArgs,
  }) async {
    final database = await open();
    return database.delete(
      table,
      where: where,
      whereArgs: whereArgs,
    );
  }

  @override
  Future<int> count(
    String table, {
    String? where,
    List<Object?>? whereArgs,
  }) async {
    final database = await open();
    final results = await database.query(
      table,
      columns: const <String>['COUNT(*) AS total'],
      where: where,
      whereArgs: whereArgs,
    );
    if (results.isEmpty) {
      return 0;
    }

    final total = results.first['total'];
    if (total is int) {
      return total;
    }
    if (total is num) {
      return total.toInt();
    }

    return int.tryParse(total.toString()) ?? 0;
  }

  @override
  Future<T> transaction<T>(Future<T> Function(Transaction txn) action) async {
    final database = await open();
    return database.transaction<T>(action);
  }

  @override
  Future<void> close() async {
    await _database?.close();
    _database = null;
  }
}
''';
  }

