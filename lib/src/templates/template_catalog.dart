  String _requestValidatorDart(ResolvedGenerationConfig resolved) {
    return '''
import '${_packageImport(resolved, 'core/error/app_failure.dart')}';
import '${_packageImport(resolved, 'core/network/utils/multipart_builder.dart')}';

class RequestValidator {
  const RequestValidator();

  AppFailure? validateBaseUrl(String baseUrl) {
    if (baseUrl.trim().isEmpty) {
      return AppFailure.validation('API base URL cannot be empty.');
    }

    final uri = Uri.tryParse(baseUrl);
    if (uri == null || !uri.hasScheme) {
      return AppFailure.validation('API base URL must be a valid URI.');
    }

    return null;
  }

  AppFailure? validateHeaders(Map<String, dynamic>? headers) {
    if (headers == null) {
      return null;
    }

    for (final entry in headers.entries) {
      if (entry.key.trim().isEmpty || entry.value == null) {
        return AppFailure.validation('Headers must have non-empty keys and values.');
      }
    }

    return null;
  }

  Future<AppFailure?> validateMultipartFiles(List<MultipartInput> files) {
    for (final file in files) {
      if (file.filePath.trim().isEmpty) {
        return Future<AppFailure?>.value(AppFailure.validation(
          'Multipart file path cannot be empty.',
        ));
      }
    }

    return Future<AppFailure?>.value(null);
  }

  AppFailure? validatePath(String path) {
    if (path.trim().isEmpty) {
      return AppFailure.validation('Request path cannot be empty.');
    }
    return null;
  }
}
''';
  }

