class MultipartBuilder {
  const MultipartBuilder._();

  static Future<FormData> build({
    required Map<String, dynamic> fields,
    List<MultipartInput> files = const <MultipartInput>[],
  }) async {
    final formData = FormData.fromMap(<String, dynamic>{...fields});

    for (final file in files) {
      formData.files.add(
        MapEntry<String, MultipartFile>(
          file.field,
          await MultipartFile.fromFile(
            file.filePath,
            filename: file.fileName ?? _fallbackFileName(file.filePath),
            contentType: file.contentType,
          ),
        ),
      );
    }

    return formData;
  }

  static String _fallbackFileName(String filePath) {
    final normalizedPath = filePath.replaceAll('\\\\', '/');
    final segments = normalizedPath.split('/');
    return segments.isEmpty ? 'upload.bin' : segments.last;
  }
}
