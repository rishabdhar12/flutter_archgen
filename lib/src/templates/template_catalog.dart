    String method,
    String path, {
    Object? data,
    Map<String, dynamic>? queryParameters,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
  }) async {
    final failure = await _validateRequest(
      path: path,
      headers: headers,
      requiresAuth: requiresAuth,
    );
    if (failure != null) {
      return Result.failure(failure);
    }

    try {
      final response = await _dio.request<dynamic>(
        path,
        data: data,
        queryParameters: queryParameters,
        options: Options(
          method: method,
          headers: _sanitizeHeaders(headers),
          extra: <String, Object?>{'requiresAuth': requiresAuth},
        ),
      );
      return Result.success(_normalizeResponse(response.data));
    } on DioException catch (error, stackTrace) {
      return Result.failure(_networkErrorMapper.map(error, stackTrace));
    } catch (error, stackTrace) {
      return Result.failure(_globalErrorMapper.map(error, stackTrace));
    }
  }

  Map<String, dynamic> _normalizeResponse(Object? data) {
    if (data == null) {
      return const <String, dynamic>{};
    }
    if (data is Map<String, dynamic>) {
      return data;
    }
    return <String, dynamic>{'data': data};
  }

  Map<String, dynamic>? _sanitizeHeaders(Map<String, dynamic>? headers) {
    if (headers == null) {
      return null;
    }

    return Map<String, dynamic>.fromEntries(
      headers.entries.where(
        (entry) => entry.key.trim().isNotEmpty && entry.value != null,
      ),
    );
