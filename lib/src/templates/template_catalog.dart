  Route<dynamic> onGenerateRoute(RouteSettings settings) {
    final routeName = settings.name ?? RouteConstants.root;

    switch (routeName) {
      case RouteConstants.root:
        return _route('Root Shell', 'Connect startup and auth flows here.');
      case RouteConstants.home:
        return _route('Home Shell', 'Replace this page with your first feature.');
      case RouteConstants.settings:
        return _route('Settings Shell', 'Wire settings once the feature exists.');
      default:
        return _route('Not Found', 'No route registered for "\$routeName".');
    }
  }

  MaterialPageRoute<void> _route(String title, String message) {
    return MaterialPageRoute<void>(
      builder: (_) => PlaceholderShellPage(title: title, message: message),
    );
  }
