  String _flavorDart(ResolvedGenerationConfig resolved) {
    final entries = resolved.config.flavors.join(', ');
    final valueCases = resolved.config.flavors
        .map(
          (flavor) => "      case Flavor.$flavor:\n        return '$flavor';",
        )
        .join('\n');
    final parseCases = resolved.config.flavors
        .map(
          (flavor) => "      case '$flavor':\n        return Flavor.$flavor;",
        )
        .join('\n');
    final defaultFlavor = resolved.config.flavors.first;

    return '''
enum Flavor { $entries }

extension FlavorX on Flavor {
  String get value {
    switch (this) {
$valueCases
    }
  }

  static Flavor fromName(String value) {
    switch (value.toLowerCase()) {
$parseCases
      default:
        return Flavor.$defaultFlavor;
    }
  }
}
''';
  }

