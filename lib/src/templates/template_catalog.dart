class ApiClientImpl implements ApiClient {
  ApiClientImpl({
    required Dio dio,
    required TokenProvider tokenProvider,
    required NetworkConfig networkConfig,
    required NetworkErrorMapper networkErrorMapper,
    required GlobalErrorMapper globalErrorMapper,
    required RequestValidator requestValidator,
  })  : _dio = dio,
        _tokenProvider = tokenProvider,
        _networkConfig = networkConfig,
        _networkErrorMapper = networkErrorMapper,
        _globalErrorMapper = globalErrorMapper,
        _requestValidator = requestValidator;

  final Dio _dio;
  final GlobalErrorMapper _globalErrorMapper;
  final NetworkConfig _networkConfig;
  final NetworkErrorMapper _networkErrorMapper;
  final RequestValidator _requestValidator;
  final TokenProvider _tokenProvider;

  @override
  Future<Result<Map<String, dynamic>>> get(
    String path, {
    Map<String, dynamic>? queryParameters,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
  }) {
    return _request(
      'GET',
      path,
      queryParameters: queryParameters,
      headers: headers,
      requiresAuth: requiresAuth,
    );
  }

  @override
  Future<Result<Map<String, dynamic>>> post(
    String path, {
    Object? data,
    Map<String, dynamic>? queryParameters,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
  }) {
    return _request(
      'POST',
      path,
      data: data,
      queryParameters: queryParameters,
      headers: headers,
      requiresAuth: requiresAuth,
    );
  }

  @override
  Future<Result<Map<String, dynamic>>> patch(
    String path, {
    Object? data,
    Map<String, dynamic>? queryParameters,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
  }) {
    return _request(
      'PATCH',
      path,
      data: data,
      queryParameters: queryParameters,
      headers: headers,
      requiresAuth: requiresAuth,
    );
  }

  @override
  Future<Result<Map<String, dynamic>>> delete(
    String path, {
    Object? data,
    Map<String, dynamic>? queryParameters,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
  }) {
    return _request(
      'DELETE',
      path,
      data: data,
      queryParameters: queryParameters,
      headers: headers,
      requiresAuth: requiresAuth,
    );
  }

  @override
  Future<Result<String>> download(
    String url, {
    required String savePath,
    Map<String, dynamic>? queryParameters,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
    void Function(int received, int total)? onReceiveProgress,
  }) async {
    final failure = await _validateRequest(
      path: url,
      headers: headers,
      requiresAuth: requiresAuth,
    );
    if (failure != null) {
      return Result.failure(failure);
    }

    try {
      await _dio.download(
        url,
        savePath,
        queryParameters: queryParameters,
        options: Options(
          headers: _sanitizeHeaders(headers),
          extra: <String, Object?>{'requiresAuth': requiresAuth},
        ),
        onReceiveProgress: onReceiveProgress,
      );
      return Result.success(savePath);
    } on DioException catch (error, stackTrace) {
      return Result.failure(_networkErrorMapper.map(error, stackTrace));
    } catch (error, stackTrace) {
      return Result.failure(_globalErrorMapper.map(error, stackTrace));
    }
  }

  @override
  Future<Result<Map<String, dynamic>>> postMultipart(
    String path, {
    required Map<String, dynamic> fields,
    List<MultipartFile>? files,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
  }) async {
    final failure = await _validateRequest(
      path: path,
      headers: headers,
      requiresAuth: requiresAuth,
    );
    if (failure != null) {
      return Result.failure(failure);
    }

    final payload = <String, dynamic>{...fields};
    if (files != null && files.isNotEmpty) {
      payload['files'] = files.length == 1 ? files.first : files;
    }

    try {
      final response = await _dio.post<dynamic>(
        path,
        data: FormData.fromMap(payload),
        options: Options(
          headers: _sanitizeHeaders(headers),
          extra: <String, Object?>{'requiresAuth': requiresAuth},
        ),
      );
      return Result.success(_normalizeResponse(response.data));
    } on DioException catch (error, stackTrace) {
      return Result.failure(_networkErrorMapper.map(error, stackTrace));
    } catch (error, stackTrace) {
      return Result.failure(_globalErrorMapper.map(error, stackTrace));
    }
  }

  Future<Result<Map<String, dynamic>>> _request(
    String method,
    String path, {
    Object? data,
    Map<String, dynamic>? queryParameters,
    Map<String, dynamic>? headers,
    bool requiresAuth = false,
  }) async {
    final failure = await _validateRequest(
      path: path,
      headers: headers,
      requiresAuth: requiresAuth,
    );
    if (failure != null) {
      return Result.failure(failure);
    }

    try {
      final response = await _dio.request<dynamic>(
        path,
        data: data,
        queryParameters: queryParameters,
        options: Options(
          method: method,
          headers: _sanitizeHeaders(headers),
          extra: <String, Object?>{'requiresAuth': requiresAuth},
        ),
      );
      return Result.success(_normalizeResponse(response.data));
    } on DioException catch (error, stackTrace) {
      return Result.failure(_networkErrorMapper.map(error, stackTrace));
    } catch (error, stackTrace) {
      return Result.failure(_globalErrorMapper.map(error, stackTrace));
    }
  }

  Map<String, dynamic> _normalizeResponse(Object? data) {
    if (data == null) {
      return const <String, dynamic>{};
    }
    if (data is Map<String, dynamic>) {
      return data;
    }
    return <String, dynamic>{'data': data};
  }

  Map<String, dynamic>? _sanitizeHeaders(Map<String, dynamic>? headers) {
    if (headers == null) {
      return null;
    }

    return Map<String, dynamic>.fromEntries(
      headers.entries.where(
        (entry) => entry.key.trim().isNotEmpty && entry.value != null,
      ),
    );
  }

  Future<AppFailure?> _validateRequest({
    required String path,
    Map<String, dynamic>? headers,
    required bool requiresAuth,
  }) async {
    final pathFailure = _requestValidator.validatePath(path);
    if (pathFailure != null) {
      return pathFailure;
    }

    final baseUrlFailure = _requestValidator.validateBaseUrl(_networkConfig.baseUrl);
    if (baseUrlFailure != null) {
      return baseUrlFailure;
    }

    final headerFailure = _requestValidator.validateHeaders(headers);
    if (headerFailure != null) {
      return headerFailure;
    }

    if (!requiresAuth) {
      return null;
    }

    final token = await _tokenProvider.readToken();
    if (token == null || token.isEmpty) {
      return AppFailure.auth('Authenticated request attempted without a token.');
    }

    return null;
  }
}
''';
  }

