    required AppEnv env,
  })  : _logger = logger,
        _isVerbose = env.enableVerboseLogs;

  final bool _isVerbose;
  final AppLogger _logger;

  @override
  void onRequest(RequestOptions options, RequestInterceptorHandler handler) {
    options.extra['startedAt'] = DateTime.now();
    if (_isVerbose) {
      _logger.info(
        'HTTP \${options.method} \${options.uri} headers=\${_sanitizeHeaders(options.headers)}',
      );
    }
    handler.next(options);
  }

  @override
  void onResponse(Response<dynamic> response, ResponseInterceptorHandler handler) {
    final startedAt = response.requestOptions.extra['startedAt'] as DateTime?;
    final durationMs = startedAt == null
        ? null
        : DateTime.now().difference(startedAt).inMilliseconds;
    _logger.info(
      'HTTP \${response.requestOptions.method} \${response.requestOptions.uri} -> \${response.statusCode} in \${durationMs ?? '-'}ms',
    );
    handler.next(response);
  }

  @override
  void onError(DioException err, ErrorInterceptorHandler handler) {
    final startedAt = err.requestOptions.extra['startedAt'] as DateTime?;
    final durationMs = startedAt == null
        ? null
        : DateTime.now().difference(startedAt).inMilliseconds;
    _logger.error(
      'HTTP \${err.requestOptions.method} \${err.requestOptions.uri} failed in \${durationMs ?? '-'}ms',
      error: err,
      stackTrace: err.stackTrace,
    );
    handler.next(err);
